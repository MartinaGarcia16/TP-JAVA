package entities;

public class Especialidad_ObralSocial {
	
	Integer id_os;
	Integer cod_especialidad;
	Float procentaje_cobertura;
	
	public Integer getId_os() {
		return id_os;
	}
	public void setId_os(Integer id_os) {
		this.id_os = id_os;
	}
	public Integer getCod_especialidad() {
		return cod_especialidad;
	}
	public void setCod_especialidad(Integer cod_especialidad) {
		this.cod_especialidad = cod_especialidad;
	}
	public Float getProcentaje_cobertura() {
		return procentaje_cobertura;
	}
	public void setProcentaje_cobertura(Float procentaje_cobertura) {
		this.procentaje_cobertura = procentaje_cobertura;
	}
}
